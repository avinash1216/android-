package nnk.com.firebase1;

public class MyFBHandler
{
    public String name,age;

    public MyFBHandler() {
    }

    public MyFBHandler(String name, String age) {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
    public void setAge(String age) {
        this.age = age;
    }

    public String getAge() {
        return age;
    }


}
